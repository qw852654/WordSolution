namespace 题库应用.题目模块.题型识别.题型特征
{
    public class 填空题规则
    {
        public (bool 命中, double 置信度, string 说明) 判断(题型特征集 特征, int 实验关键词命中数量)
        {
            var 空位强度 = 特征.显式下划线数量 + 特征.下划线格式空白数量;

            if (特征.完整ABCD结构组数 > 0)
            {
                return (false, 0, "存在完整ABCD结构，不判为填空题。");
            }

            if (特征.归一化后段落数 is 1 or 2)
            {
                return (true, 0.88, "在排除选择题后，归一化后段落数为 1 或 2 段，判为填空题。");
            }

            if (空位强度 > 0 && 特征.归一化后段落数 <= 4 && !特征.归一化文本.Contains("实验"))
            {
                return (true, 0.76, "检测到空位结构，且段落数不多，判为填空题。");
            }

            return (false, 0, "未命中填空题的段落特征或空位特征。");
        }
    }
}
