namespace 题库应用.题目模块.题型识别.题型特征
{
    public class 实验题规则
    {
        public (bool 命中, double 置信度, string 说明) 判断(题型特征集 特征, int 关键词命中数量)
        {
            if (!特征.归一化文本.Contains("实验"))
            {
                return (false, 0, "未出现“实验”关键词，不判为实验题。");
            }

            var 段落明显较多 = 特征.归一化后段落数 > 3;
            var 命中实验关键词 = 关键词命中数量 >= 1;

            if (段落明显较多)
            {
                return (true, 0.9, "命中“实验”关键词且归一化后段落数超过 3 段，判为实验题。");
            }

            var 段落较多 = 特征.归一化后段落数 >= 3;
            var 命中多问结构 = 特征.是否存在多小问结构;

            if (段落较多 && (命中实验关键词 || 命中多问结构))
            {
                return (true, 0.82, "出现“实验”关键词，且段落和结构特征支持实验题。");
            }

            if (命中实验关键词 && 命中多问结构)
            {
                return (true, 0.78, "出现“实验”关键词，且存在多小问结构。");
            }

            return (false, 0, "实验题强特征不足。");
        }
    }
}
